
<h2 style="padding: 0; margin-top: 0; margin-bottom: 15px">Licence Professionnelles en Organisation et Management (LPOM)</h2>
<p><h4>1-Présentation et Objectifs</h4>
	C’est une filière à cycle unique, débouchant sur un diplôme professionnel de deuxième cycle : la Maîtrise Professionnelle en Organisation et en Management.
	La formation a pour objectif de transformer le personnel agent en personnel d’encadrement en enrichissant la formation professionnelle du candidat. Ainsi la formation doit amener le candidat à :
	<ul>
	<li>renforcer les connaissances techniques acquises lors de la formation antérieure</li>
	<li>valoriser l’expérience pratique acquise en milieu professionnel</li>
	<li>assumer des responsabilités d’encadrement supérieur </li>
	<li>améliorer ses pratiques managériales </li>
	<li>Développer des qualités humaines compatibles avec l’éthique de la profession en management</li>
	</ul>
	<h4>2-Admission</h4>
	Sur étude de dossier pour des candidats titulaires d’un BTS ou d’un DUT, ayant une expérience professionnelle d’au moins 2 ans..
 </p>