
<h2 style="padding: 0; margin-top: 0; margin-bottom: 15px">Etudes Professionnelles Approfondies (EPA)</h2>
<p><h4>1-Présentation et Objectifs</h4>
	C’est une filière à cycle unique débouchant sur un diplôme de second cycle : le Diplôme d’Etudes Professionnelles Approfondies (D.E.P.A.). Elle a pour objectif d’adapter la formation initiale du candidat aux exigences professionnelles en lui fournissant les outils et les méthodes de base en Management, afin de lui permettre de:
	<ul>
	<li>se familiariser avec le langage courant de la gestion</li>
	<li>maîtriser l’utilisation des outils modernes dans la résolution des problèmes de gestion</li>
	<li>Développer des qualités humaines compatibles avec l’éthique de la profession en management</li>
	</ul>
	<h4>2-Admission</h4>
	Par voie de concours ouvert aux titulaires d’une Licence ou de tout diplôme jugé équivalent. Une admission spéciale peut être obtenue sur étude de dossier pour les candidats de nationalité étrangère.
 </p>