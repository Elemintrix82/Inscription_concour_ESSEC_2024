
<h2 style="padding: 0; margin-top: 0; margin-bottom: 15px">Master of Business Adminstration (MBA)</h2>
<p><h4>1-Présentation et Objectifs</h4>
	C’est une filière à cycle unique débouchant sur un diplôme professionnel de second cycle : le Master of Business Administration (M.B.A.).
	La formation a pour objectif de compléter et d’enrichir la formation initiale du candidat. Ainsi la formation doit amener le candidat à :
	<ul>
	<li>Acquérir une formation large dans les différentes disciplines en management</li>
	<li>Maîtriser les fonctions de l’entreprise renforcer les connaissances techniques acquises lors de la formation antérieure </li>
	<li>Valoriser l’expérience pratique acquise en milieu professionnel</li>
	<li>Assumer des responsabilités d’encadrement supérieur </li>
	<li>Exercer efficacement les fonctions d’attaché de direction</li>
	<li>Améliorer ses pratiques managériales</li>
	<li>Développer des qualités humaines compatibles avec l’éthique de la profession en management.</li>
	</ul>
	<h4>2-Admission</h4>
	Sur étude de dossier pour des candidats titulaires d’une Licence ou de tout diplôme jugé équivalent, ayant une expérience professionnelle d’au moins 2 ans.
 </p>