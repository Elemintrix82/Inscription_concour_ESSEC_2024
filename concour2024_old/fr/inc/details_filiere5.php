
<h2 style="padding: 0; margin-top: 0; margin-bottom: 15px">Master 2 Recherche </h2>
<p><h4>1-Présentation et Objectifs</h4>
	Elle pour objectifs de :
	<ul>
	<li>former à la recherche des étudiants se destinant particulièrement à des activités d’enseignement et d’études de niveau supérieur hautement spécialisées dans les domaines des sciences de gestion</li>
	<li>Former par la recherche des responsables de décision de haut niveau dans les entreprises et les organisations à but non lucratif, ainsi que les conseillers spécialisés dans les domaines de la gestion.</li>
	</ul>
	<h4>2-Admission</h4>
	Sur étude de dossier pour des candidats titulaires d’une Maîtrise en Sciences de Gestion ou en Economie, ou de tout diplôme jugé équivalent.
 </p>