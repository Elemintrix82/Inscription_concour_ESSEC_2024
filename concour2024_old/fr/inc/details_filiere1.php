
<h2 style="padding: 0; margin-top: 0; margin-bottom: 15px">Etudes Supérieurs de Commerce (ESC)</h2>
<p><h4>1-Présentation et Objectifs</h4>
	Cette formation a pour objectif d’amener l’étudiant à :
	<ul>
	<li>acquérir les concepts de base ouvrant à la formation de manager</li>

	<li>posséder et utiliser le langage courant de la gestion</li>

	<li>comprendre l’environnement et le fonctionnement de l’entreprise ;</li>

	<li>se spécialiser après deux ans de tronc commun,  dans un domaine de la gestion.</li>
	</ul>
	<h4>2-Admission</h4>
	Sur concours, ouvert aux titulaires du Baccalauréat A, B, C, D, G ou du GCE A/L avec mathématiques et/ou matières d’économie et de gestion pour les premières années. Toutefois, une admission spéciale sur étude de dossier par le jury du concours peut être obtenue par les candidats de nationalité étrangère.

	De même dans la limite des places disponibles, un concours peut être ouvert pour l’admission au niveau II du premier cycle, aux titulaires du DUT, du BTS, du DEUP, du DEUG en économie ou en gestion, ou de tout diplôme jugé équivalent.
 </p>