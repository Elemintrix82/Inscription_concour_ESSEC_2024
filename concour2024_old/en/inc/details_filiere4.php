<h2 style="padding: 0; margin-top: 0; margin-bottom: 15px">Master of Business Administration (MBA)</h2>
<p><h4>1-Presentation and Objectives</h4>
	This is a single-cycle program leading to a second-cycle professional diploma: the Master of Business Administration (M.B.A.).
	The training aims to complete and enrich the candidate's initial education. Thus, the training should enable the candidate to:
	<ul>
	<li>Acquire broad training in various management disciplines</li>
	<li>Master company functions and reinforce the technical knowledge acquired during previous training</li>
	<li>Enhance the practical experience gained in a professional environment</li>
	<li>Assume higher management responsibilities</li>
	<li>Effectively perform the functions of a management consultant</li>
	<li>Improve managerial practices</li>
	<li>Develop human qualities compatible with the ethics of the management profession.</li>
	</ul>
	<h4>2-Admission</h4>
	Based on the examination of the application for candidates holding a Bachelor's degree or any equivalent diploma, with at least 2 years of professional experience.
</p>
