
<h2 style="padding: 0; margin-top: 0; margin-bottom: 15px">Higher School of Commerce (ESC)</h2>
<p><h4>1-Presentation and Objectives</h4>
	This program aims to:
	<ul>
	<li>acquire the basic concepts required for management training</li>

	<li>possess and use common management terminology</li>

	<li>understand the environment and functioning of a business</li>

	<li>specialize in a management field after two years of common core studies.</li>
	</ul>
	<h4>2-Admission</h4>
	Admission is based on a competitive exam, open to holders of the Baccalaureate A, B, C, D, G or GCE A/L with mathematics and/or economics and management subjects for the first years. However, special admission based on the examination of the application by the jury can be granted to foreign candidates.

	Similarly, within the limits of available places, a competitive exam can be opened for admission to the second year of the first cycle, for holders of DUT, BTS, DEUP, DEUG in economics or management, or any equivalent diploma.
</p>
