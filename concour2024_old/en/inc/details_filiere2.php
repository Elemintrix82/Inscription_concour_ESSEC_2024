<h2 style="padding: 0; margin-top: 0; margin-bottom: 15px">Advanced Professional Studies (EPA)</h2>
<p><h4>1-Presentation and Objectives</h4>
	This is a single-cycle program leading to a second-cycle diploma: the Diploma of Advanced Professional Studies (D.E.P.A.). Its objective is to adapt the candidate's initial training to professional requirements by providing basic tools and methods in Management, in order to enable them to:
	<ul>
	<li>familiarize themselves with common management terminology</li>
	<li>master the use of modern tools in solving management problems</li>
	<li>develop human qualities compatible with the ethics of the management profession</li>
	</ul>
	<h4>2-Admission</h4>
	Admission is based on a competitive exam open to holders of a Bachelor's degree or any equivalent diploma. Special admission can be granted based on the examination of the application for foreign candidates.
</p>
