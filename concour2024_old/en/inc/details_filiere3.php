<h2 style="padding: 0; margin-top: 0; margin-bottom: 15px">Professional Bachelor's in Organization and Management (LPOM)</h2>
<p><h4>1-Presentation and Objectives</h4>
	This is a single-cycle program leading to a second-cycle professional diploma: the Professional Master's in Organization and Management.
	The training aims to transform staff agents into managerial staff by enriching the candidate's professional training. Thus, the training should enable the candidate to:
	<ul>
	<li>reinforce the technical knowledge acquired during previous training</li>
	<li>enhance the practical experience gained in a professional environment</li>
	<li>assume higher management responsibilities</li>
	<li>improve managerial practices</li>
	<li>develop human qualities compatible with the ethics of the management profession</li>
	</ul>
	<h4>2-Admission</h4>
	Based on the examination of the application for candidates holding a BTS or DUT, with at least 2 years of professional experience.
</p>
